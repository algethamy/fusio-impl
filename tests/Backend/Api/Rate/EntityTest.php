        ));

        $routeId = Fixture::getLastRouteId() + 1;
        $body    = (string) $response->getBody();
        $expect  = <<<JSON
{
    "id": 4,
    "status": 1,
    "priority": 10,
    "name": "gold",
    "rateLimit": 16,
    "timespan": "P1M",
    "allocation": [
        {
            "id": 4,
